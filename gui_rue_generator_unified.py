def get_unique_path(path: str) -> str:
    """
    Wenn 'path' schon existiert, wird ' (2)', ' (3)', ... vor der Endung angehängt,
    bis ein freier Dateiname gefunden ist – (wie bei Windows).
    """
    if not os.path.exists(path):
        return path
    base, ext = os.path.splitext(path)
    counter = 2
    while True:
        candidate = f"{base} ({counter}){ext}"
        if not os.path.exists(candidate):
            return candidate
        counter += 1

def clamp_geometry(geom: str, sw: int, sh: int, default_w=980, default_h=720) -> str:
    """
    Erzwingt eine sichtbare Fensterposition.
    Erwartet geom wie '980x720+120+80' oder '980x720-10+80'.
    """
    m = re.match(r"(\d+)x(\d+)([+-]\d+)([+-]\d+)", str(geom).strip())
    if not m:
        w, h = default_w, default_h
        x, y = 120, 80
    else:
        w, h = int(m.group(1)), int(m.group(2))
        x, y = int(m.group(3)), int(m.group(4))

    w = min(max(400, w), sw)
    h = min(max(300, h), sh)

    x = min(max(0, x), max(0, sw - w))
    y = min(max(0, y), max(0, sh - h))

    return f"{w}x{h}+{x}+{y}"

