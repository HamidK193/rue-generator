def build_lines_visev(vins: List[str], include_header: bool) -> List[str]:
    """
    Baut die ViseV-TXT:
    - optional Header: 'VIN<TAB>Campaign Description'
    - danach: für JEDE VIN alle Aktionen aus VISEV_ACTIONS_7088
      Format je Zeile: '<VIN-mit-Bindestrich>\t<AKTION>*'
    """
    lines: List[str] = []
    if include_header:
        lines.append("VIN\tCampaign Description")

    for vin in vins:
        if len(vin) == 17:
            vin_fmt = f"{vin[:10]}-{vin[10:]}"
        else:
            vin_fmt = vin

        for aktion in VISEV_ACTIONS_7088:
            lines.append(f"{vin_fmt}\t{aktion}*")

    return lines

