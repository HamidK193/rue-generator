def build_line_zasm(aktion: str, pnr: str, stempel: str, ts: str) -> str:
    left = f"{aktion} {CONST1} {pnr:>7}"
    spaces_needed = AFTER_PNR_COL - len(left)
    if spaces_needed < 0:
        left = left[:AFTER_PNR_COL]
        spaces_needed = 0
    left += " " * spaces_needed
    right = f"{stempel}{' '}{CONST2}{' '}{ts}"
    line = left + right
    if len(line) > LINE_LEN:
        line = line[:LINE_LEN]
    elif len(line) < LINE_LEN:
        line += " " * (LINE_LEN - len(line))
    return line

def build_preview_zasm(project: str, stempel: str, pnrs: List[str], ts: str) -> List[str]:
    aktionen = PROJEKTE[project]["AKTIONEN"]
    return [build_line_zasm(a, p, stempel, ts) for p in pnrs for a in aktionen]

