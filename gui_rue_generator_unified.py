def parse_pnrs(text: str, remove_dupes=True) -> List[str]:
    """Für ZASM: extrahiert Ziffernfolgen; optional dedupe (Order beibehalten)."""
    tokens = re.split(r"[^A-Z0-9]+", text.upper())
    digits = [re.sub(r"\D+", "", t) for t in tokens if t]
    digits = [d for d in digits if d]
    if remove_dupes:
        digits = list(dict.fromkeys(digits))
    return digits

VIN_RE = re.compile(r"[A-HJ-NPR-Z0-9]{17}")  # klassische VIN-Regel (ohne I,O,Q)
def parse_vins(text: str, keep_dupes=True) -> List[str]:
    """Für ViseV: extrahiert echte VINs (17 Zeichen). Duplicates je nach Flag."""
    vins = VIN_RE.findall(text.upper())
    if keep_dupes:
        return vins
    seen, out = set(), []
    for v in vins:
        if v not in seen:
            out.append(v)
            seen.add(v)
    return out

